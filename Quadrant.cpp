#include<iostream>
using namespace std;
int main (){

    int x,y;
    cout<<"Enter The Value of X : ";
    cin>>x;

    cout<<"Enter The Value Of Y : ";
    cin>>y;

    if(x==x and y==y){
        cout<<"First Quadrant ";
    }

        if(x==-x and y==y){
        cout<<"Second Quadrant ";
    }

        if(x==-x and y==-y){
        cout<<"Third Quadrant ";
    }

        if(x==x and y==-y){
        cout<<"Forth Quadrant ";
    }
}